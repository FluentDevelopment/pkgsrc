#!${PREFIX}/bin/perl

while (<STDIN>) {

	/^\[Site/ && print $_;

	/^\[/ && next; 
	/^$/ && next;

	$_ =~ s/[0-9]+\.//g;
	$_ =~ s/0-1//g;
	$_ =~ s/1-0//g;
	$_ =~ s/1\/2-1\/2//g;
	$_ =~ s/^ +//g;
	$_ =~ s/ +/ /g;
	/^$/ && next;
	print "$_";

	while (<STDIN>) {
		/^$/ && last;
		$_ =~ s/[0-9]+\.//g;
		$_ =~ s/0-1//g;
		$_ =~ s/1-0//g;
		$_ =~ s/1\/2-1\/2//g;
		$_ =~ s/^ +//g;
		$_ =~ s/ +/ /g;
		/^$/ && next;
		print "$_";
	}

}
	
